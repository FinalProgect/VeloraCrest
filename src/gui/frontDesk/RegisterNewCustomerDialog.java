    private void saveIconMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveIconMouseClicked

        if (jTextField4.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Please enter customer first name.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (jTextField1.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Please enter customer last name.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (jTextField3.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Please enter customer NIC number.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (jTextField2.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Please enter customer Email.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (jTextField5.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Please enter customer Mobile Number.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!jTextField5.getText().matches("^07[0,1,2,4,5,6,7,8]{1}[0-9]{7}$")) {
            JOptionPane.showMessageDialog(this, "invalid Mobile number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            String fname = jTextField4.getText();
            String lname = jTextField1.getText();
            String nic = jTextField3.getText();
            String email = jTextField2.getText();
            String mobile = jTextField5.getText();

            try {

                String quary = "SELECT * FROM `customer` WHERE `customer`.`email` = '" + email + "' AND `customer`.`nic` = '" + nic + "' AND `customer`.`mobile` = '" + mobile + "'";

                ResultSet resulr = MYsql.execute(quary);

                if (resulr.next()) {

                    if (resulr.getString("customer.customerStatus_id").equals("2")) {

                        int condition = JOptionPane.showConfirmDialog(this, "This customer Is Innactive. Do You want to active?", "Alert", JOptionPane.INFORMATION_MESSAGE);

                        if (condition == JOptionPane.OK_OPTION) {

                            String updateQuary = "UPDATE `customer` SET `customer`.`customerStatus_id` = 1 WHERE `customer`.`id` = '" + resulr.getString("customer.id") + "'";

                            MYsql.execute(updateQuary);

                            //  Setup notifications with jfram
                            Notifications.getInstance().setJFrame(parent);

                            //  Show notifications
                            Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.BOTTOM_RIGHT, 10000, "Customer registered Successfully");

                            resetFilds();

                            loadCustomers();

                        }

                    }

                    JOptionPane.showMessageDialog(this, "Customer Alredy Registerd.", "Warrning", JOptionPane.WARNING_MESSAGE);
                } else {

                    Date date = new Date();
                    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");

                    String insertQuary = "INSERT INTO `customer` "
                            + "(`fname`,`lname`,`email`,`mobile`,`registerdDate`,`nic`, `customerStatus_id`) "
                            + "VALUES ('" + fname + "', '" + lname + "', '" + email + "', '" + mobile + "', '" + sdf.format(date) + "', '" + nic + "', '1')";

                    MYsql.execute(insertQuary);

                    //  Setup notifications with jfram
                    Notifications.getInstance().setJFrame(parent);

                    //  Show notifications
                    Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.BOTTOM_RIGHT, 10000, "Customer registered Successfully");

                    resetFilds();

                    loadCustomers();

                }

            } catch (SQLException e) {
                e.printStackTrace();
                SignIn.logger.severe(e.getMessage());
            }

        }
    }//GEN-LAST:event_saveIconMouseClicked

