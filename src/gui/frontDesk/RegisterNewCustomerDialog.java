    //Load Customers
    private void loadCustomers() {

        String quary = "SELECT * FROM `customer` WHERE `customer`.`customerStatus_id` = '1' ";

        try {

            ResultSet result = MYsql.execute(quary);

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (result.next()) {

                Vector<String> vector = new Vector<>();
                vector.add(result.getString("customer.id"));
                vector.add(result.getString("customer.fname"));
                vector.add(result.getString("customer.lname"));
                vector.add(result.getString("customer.nic"));
                vector.add(result.getString("customer.mobile"));
                vector.add(result.getString("customer.email"));
                vector.add(result.getString("customer.registerdDate"));

                model.addRow(vector);

            }

            jTable1.setModel(model);

        } catch (SQLException e) {
            e.printStackTrace();
        }

    }
